twoDList = [
    [1, 3, 5],
    [3, 6, 8],
    [1, 2],
    [4, 6, 3, 12, 32]
]
# a 2 dimensional list. Each list has an index within "twoDList", and each of the individual elements within the sublists have their own indexes as well
# kind of like x and y coordinates. We can go deeper and have 3d lists, 4d lists, etc.

print(twoDList[2][1]) # first index specifies which list within 2d lists we want to access. second index specifies which element within the selected list we want to access

